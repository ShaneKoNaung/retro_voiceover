import base64
import requests
import os

# OpenAI API Key
# Read the OpenAI API key from an environment variable
api_key = os.getenv('OPENAI_ACCESS_TOKEN')
if not api_key:
    raise ValueError("Please set the OPENAI_ACCESS_TOKEN environment variable.")
                     
# Function to encode the image
def encode_image(image_path):
  with open(image_path, "rb") as image_file:
    return base64.b64encode(image_file.read()).decode('utf-8')

# Path to your image
image_path = "output_image_23.jpg" #23

# Getting the base64 string
base64_image = encode_image(image_path)

headers = {
  "Content-Type": "application/json",
  "Authorization": f"Bearer {api_key}"
}

payload = {
  "model": "gpt-4-vision-preview",
  "messages": [
    {
      "role": "user",
      "content": [
        {
          "type": "text",
          "text": "Can you read the text in the image for me? can you tell me exact locations of each piece of text?"
        },
        {
          "type": "image_url",
          "image_url": {
            "url": f"data:image/jpeg;base64,{base64_image}"
          }
        }
      ]
    }
  ],
  "max_tokens": 300
}

response = requests.post("https://api.openai.com/v1/chat/completions", headers=headers, json=payload)

print(response.json())